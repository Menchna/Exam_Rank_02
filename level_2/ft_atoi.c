int	ft_atoi(const char	*str)
{
	int	minus = 1;
	int	num = 0;
	int	i = 0;

	while (str[i] == ' ' || str[i] == '\f' || str[i] == '\n' || str[i] == '\t'
		|| str[i] == '\r' || str[i] == '\v')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			minus *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + (str[i] - '0');
		i++;
	}
	return (num * minus);
}
